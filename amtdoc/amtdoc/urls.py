from django.contrib import admin
from django.urls import path, include
from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('api/', include('agency.urls')),
    path('api/token/', TokenObtainPairView.as_view(), name='token_obtain_pair'),
    path('api/token/refresh/', TokenRefreshView.as_view(), name='token_refresh'),
    path('', include('consult.urls')),
]
# one can obtain JWT access and refresh tokens by sending a POST request to the /api/token/ endpoint with valid user credentials. Then, you can include the access token in the Authorization header for authenticated API requests.